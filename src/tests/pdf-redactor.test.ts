describe('PDF Redactor', () => {
  describe('applyVisualRedactions', () => {
    it('returns modified PDF bytes for text detections', async () => {
      const detections: Detection[] = [
        {
          id: '1',
          type: 'EMAIL',
          text: 'test@example.com',
          page: 1,
          bbox: { x: 100, y: 700, width: 100, height: 12 },
          confidence: 1,
          source: 'regex',
        },
      ];

      const result = await applyVisualRedactions(mockPdfBytes, detections);

      expect(result).toBeInstanceOf(Uint8Array);
      expect(result.length).toBeGreaterThan(mockPdfBytes.length); // Should be larger due to added redaction rectangles
    });

    it('skips OCR detections', async () => {
      const detections: Detection[] = [
        {
          id: '1',
          type: 'EMAIL',
          text: 'test@example.com',
          page: 1,
          bbox: { x: 100, y: 700, width: 100, height: 12 },
          confidence: 1,
          source: 'ocr',
        },
      ];

      const result = await applyVisualRedactions(mockPdfBytes, detections);

      // Should be similar size since OCR detections are skipped
      expect(result.length).toBeCloseTo(mockPdfBytes.length, -10);
    });

    it('handles empty detections', async () => {
      const result = await applyVisualRedactions(mockPdfBytes, []);

      expect(result).toBeInstanceOf(Uint8Array);
      expect(result.length).toBeGreaterThan(0);
    });

    it('throws for invalid PDF', async () => {
      const invalidPdf = new Uint8Array([0x00, 0x01, 0x02]);

      await expect(applyVisualRedactions(invalidPdf, [])).rejects.toThrow();
    });
  });
});